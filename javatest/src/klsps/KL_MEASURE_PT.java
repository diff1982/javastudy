package klsps;

public class KL_MEASURE_PT {	
	String m_dateTime;//日期-时间
	double m_lineNo;//线号 	
	double m_ptNo;//点号	
	String m_ptName;//点类型
	double m_x;//维度
	double m_y;//经度
	
	public String getM_dateTime() {
		return m_dateTime;
	}
	public void setM_dateTime(String m_dateTime) {
		this.m_dateTime = m_dateTime;
	}
	public double getM_lineNo() {
		return m_lineNo;
	}
	public void setM_lineNo(double m_lineNo) {
		this.m_lineNo = m_lineNo;
	}
	public double getM_ptNo() {
		return m_ptNo;
	}
	public void setM_ptNo(double m_ptNo) {
		this.m_ptNo = m_ptNo;
	}
	public String getM_ptName() {
		return m_ptName;
	}
	public void setM_ptName(String m_ptName) {
		this.m_ptName = m_ptName;
	}
	public double getM_x() {
		return m_x;
	}
	public void setM_x(double m_x) {
		this.m_x = m_x;
	}
	public double getM_y() {
		return m_y;
	}
	public void setM_y(double m_y) {
		this.m_y = m_y;
	}	
}